            también para enviar imágenes al servidor según la funcionalidad de
            la aplicación.
          </li>
          <li>
            <strong>Galería:</strong>
            También solicitamos acceso a la galería para seleccionar y enviar
            imágenes relevantes al servidor como parte de la gestión de
            información de los dispositivos.
          </li>
        </ul>
      </div>

